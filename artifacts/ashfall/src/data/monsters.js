export function getMonstersByTerrain(terrain, dangerLevel = 1) {
  return Object.values(MONSTER_TEMPLATES).filter(m =>
    m.habitat.includes(terrain) && m.dangerLevel <= dangerLevel + 1
  );
}

export function getMonstersByDanger(maxDanger) {
  return Object.values(MONSTER_TEMPLATES).filter(m => m.dangerLevel <= maxDanger);
}

export function getDungeonBoss(dangerLevel) {
  const bosses = Object.values(MONSTER_TEMPLATES).filter(m => m.isBoss && m.dangerLevel <= dangerLevel);
  if (bosses.length === 0) return MONSTER_TEMPLATES.troll;
  return bosses[Math.floor(Math.random() * bosses.length)];
}
